# start = valor inicial da sequencia 
# stop = valor final da sequencia (ultimo valor fica de fora)
# step = diferença entre os valores grados na sequencia 
# só pode números inteiros 

for num in range(5):
    print(num)

for num1 in range(10, 21):
    print(num1)

for num2 in range(40, 51, 2):
    print(num2)

for num3 in range (10, 0, -2):       #decrecente valor negativo
    print(num3)



